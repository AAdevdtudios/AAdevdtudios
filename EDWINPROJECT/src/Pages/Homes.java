    public Homes() {
        initComponents();
        this.setTitle("Clean");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //ID, Matric, Department, Name, Score (Int), Grade
        try{
            int ammount;
            Class.forName("com.mysql.jdbc.Driver");
            conections = DriverManager.getConnection("jdbc:mysql://localhost:3306/clean", "root", "");
            statement = conections.prepareStatement("SELECT * FROM record");
            results = statement.executeQuery();
            ResultSetMetaData resultSet = results.getMetaData();
            ammount = resultSet.getColumnCount();
            model.setRowCount(0);
            while(results.next()){
                Vector vectorData = new Vector();
                boolean res;
                for(int i = 1; i<= ammount; i++){
                    vectorData.add(results.getString("Name"));
                    vectorData.add(results.getString("Address"));
                    vectorData.add(results.getString("Phone"));
                    vectorData.add(results.getString("DPO"));
                }
                model.addRow(vectorData);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }
    }

