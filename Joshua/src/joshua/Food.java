    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
       DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
       int selectIndex = jTable1.getSelectedRow();
       foodname.setText(dft.getValueAt(selectIndex, 1).toString());
       recipes.setText(dft.getValueAt(selectIndex, 2).toString());
       cookTime.setText(dft.getValueAt(selectIndex, 3).toString());
       editBtn.setEnabled(true);
       removeBtn.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(cookTime.getText().isEmpty() || foodname.getText().isEmpty()||recipes.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "One of the fields is empty");
        }else{
            AddItems add= new AddItems();
            add.AddRecipes(this, foodname.getText().trim(), recipes.getText().trim(), cookTime.getText().trim());
            vm.Recipes(this, jTable1);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        // TODO add your handling code here:
       DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
       int selectIndex = jTable1.getSelectedRow();
        upDate dl = new upDate();
        dl.UpdateFoodRecipes(this,foodname.getText().trim(),recipes.getText().trim(),cookTime.getText().trim(), dft.getValueAt(selectIndex, 0).toString().trim());
        vm.Recipes(this, jTable1);
    }//GEN-LAST:event_editBtnMouseClicked

    private void removeBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeBtnMouseClicked
        // TODO add your handling code here:
       DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
       int selectIndex = jTable1.getSelectedRow();
        Delete dl = new Delete();
        dl.DeleteRecipes(this, dft.getValueAt(selectIndex, 0).toString());
        vm.Recipes(this, jTable1);
    }//GEN-LAST:event_removeBtnMouseClicked

