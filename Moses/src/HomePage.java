    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
       DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
       int selected = jTable1.getSelectedRow();
       drn.setText(dft.getValueAt(selected, 0).toString());
       plateno.setText(dft.getValueAt(selected, 1).toString());
       if(dft.getValueAt(selected, 2).toString().equals("true")){
        check.setSelected(true);
       }else{
           check.setSelected(false);
       }
       owners.setText(dft.getValueAt(selected, 3).toString());
       models.setText(dft.getValueAt(selected, 4).toString());
    }//GEN-LAST:event_jTable1MouseClicked

